"""
Pydantic schemas for request validation and response serialization.

All schemas include OpenAPI examples for Swagger UI documentation.
"""

from datetime import datetime
from typing import List, Optional

from pydantic import BaseModel, Field


# ── Single Audio Record ──────────────────────────────────────────────

class AudioRecordSchema(BaseModel):
    """A single audio record stored in the pipeline database."""

    id: int = Field(..., description="Unique record identifier", examples=[1])
    device_id: str = Field(..., description="Wearable device that uploaded this audio", examples=["device_101"])
    file_path: str = Field(
        ...,
        description="Relative path to the stored audio file",
        examples=["audio_storage/device_101/20260312_181500_a1b2c3d4_recording.wav"],
    )
    file_name: str = Field(..., description="Original uploaded filename", examples=["recording.wav"])
    file_size: int = Field(..., description="File size in bytes", examples=[64044])
    duration: Optional[float] = Field(None, description="Audio duration in seconds", examples=[2.5])
    transcription: Optional[str] = Field(
        None,
        description="Text transcription generated by Faster-Whisper",
        examples=["Hello, this is a test recording from the wearable device."],
    )
    language: Optional[str] = Field(
        None,
        description="Detected language code (ISO 639-1)",
        examples=["en"],
    )
    language_confidence: Optional[float] = Field(
        None,
        description="Language detection confidence score (0.0 to 1.0)",
        examples=[0.9856],
    )
    created_at: datetime = Field(
        ...,
        description="Upload timestamp (UTC)",
        examples=["2026-03-12T18:15:00"],
    )

    model_config = {
        "from_attributes": True,
        "json_schema_extra": {
            "examples": [
                {
                    "id": 1,
                    "device_id": "device_101",
                    "file_path": "audio_storage/device_101/20260312_181500_a1b2c3d4_recording.wav",
                    "file_name": "recording.wav",
                    "file_size": 64044,
                    "duration": 2.5,
                    "transcription": "Hello, this is a test recording from the wearable device.",
                    "language": "en",
                    "language_confidence": 0.9856,
                    "created_at": "2026-03-12T18:15:00",
                }
            ]
        },
    }


# ── Upload Response ──────────────────────────────────────────────────

