    """Response returned after a successful audio upload and transcription."""

    id: int = Field(..., description="Newly created record ID", examples=[1])
    device_id: str = Field(..., description="Device that uploaded the audio", examples=["device_101"])
    file_name: str = Field(..., description="Original uploaded filename", examples=["recording.wav"])
    file_size: int = Field(..., description="Saved file size in bytes", examples=[64044])
